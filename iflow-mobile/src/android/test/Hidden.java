package android.test;

public class Hidden {

	private String name;
	private String value;
	
	public Hidden() {
		super();
		
		this.name = "";
		this.value = "";
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	
}
